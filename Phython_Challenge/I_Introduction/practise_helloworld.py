# Introduction

print(4 + 5)   # addition(+)
print(5 - 2)   # subtraction(-)
print(5 * 6)   # multiplication(*)
print(5 / 4)   # division(/)
print(5 ** 4)  # exponential(**)
print(5 % 4)   # modulus(%)
print(5 // 4)  # Floor division operator(//)

# Checking data types

print(type(20))                     # Int
print(type(3.14))                   # Float
print(type(1 + 3j))                 # Complex
print(type('Muyiwa'))               # String
print(type([4, 5, 6]))              # List
print(type({'name': 'Muyiwa'}))     # Dictionary
print(type({9.8, 3.14, 2.7}))       # Set
print(type((9.8, 3.14, 2.7)))       # Tuple

